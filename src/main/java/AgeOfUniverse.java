public class AgeOfUniverse {

    public static void main(String[] args) {

        final long ageOfUniverse = 13000000000L;
        System.out.println("ageOfUniverse = " + ageOfUniverse);

        final int ageOfHumanity = 100000;
        System.out.println("ageOfHumanity = " + ageOfHumanity);

        final long ageDifference = ageOfUniverse / ageOfHumanity;
        System.out.println("ageDifference = " + ageDifference);
    }
}
